package com.duckduckgo.app.prayers.utils

import android.content.BroadcastReceiver
import android.content.Context
import android.content.Intent

class AlarmReceiver : BroadcastReceiver() {

    override fun onReceive(context: Context?, intent: Intent?) {
        val message = intent?.getStringExtra("EXTRA_MESSAGE") ?: return
        val notificationId = intent.getIntExtra("notificationId", 0)
        val triggerTime = intent.getLongExtra("when", 0L)

        context?.let {
            NotificationUtils.createNotification(it, message, notificationId, triggerTime)
            NotificationUtils.removeNotificationEntryFromSharedPref(it, notificationId)
        }
    }
}
